// Math.abs()
// Math.abs(x) returns the absolute (positive) value of x.

console.log(Math.abs(-55));
console.log(Math.abs(-55.5));
console.log(Math.abs(-995));
console.log(4-6);
console.log(Math.abs(4-6));



// Math.ceil()
// Math.ceil(x) returns the value of x rounded up to its nearest integer

console.log(Math.ceil(4.4));
console.log(Math.ceil(99.1));



// Math.floor()
// Math.floor(x) returns the value of x rounded to its nearest integer

console.log(Math.floor(4.7));
console.log(Math.floor(99.9));



// Math.max()
// Math.max() can be used to find the highest value in the list of arguments.

console.log(Math.max(0, 150, 30, 20, -8, -200));


// Math.random()
// Math.random() returns a random number between 0 and 1 (excluding 1)

console.log(Math.floor(Math.random() * 10));



// Math.trunc()
// The trunc() method returns the integer part of a number

console.log(Math.trunc(4.6));
console.log(Math.trunc(-99.1));






